//Half Pyreamid with numbers
public class PatternSix {
    public static void main(String[] args) {
        //Prints Five Rows
        for(int i = 0 ; i <=5 ;i++){
            for (int j = 1 ; j <= i ;j++){
                System.out.print(j);
            }
            System.out.println();

        }
    }
}
