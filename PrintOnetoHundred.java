// Print One till Hundred without using Numbers
public class PrintOnetoHundred {
    public static void main(String[] args) {
        int One = 'A' / 'A';
        String S ="..........";
        for (int i = One ; i <= S.length() * S.length() ;i++){
            System.out.println(i);
        }
    }
    
}
